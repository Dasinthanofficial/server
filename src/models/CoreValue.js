import mongoose from "mongoose";

const CoreValueSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    order: {
      type: Number,
      default: 0,
    },
    active: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

CoreValueSchema.index({ order: 1 });

export const CoreValue = mongoose.model("CoreValue", CoreValueSchema);